# Exercício 4 - Transformações de Capitalização em Strings
#
# Uma plataforma de normalização de dados textuais está padronizando nomes,
# cargos e descrições. Crie uma string nome_completo com seu nome completo
# e imprima os seguintes resultados:
# tudo em maiúsculo (upper), tudo em minúsculo (lower),
# primeira letra de cada palavra maiúscula (title),
# primeira letra da string maiúscula (capitalize),
# e inversão de maiúsculas/minúsculas (swapcase).

nome_completo = 'Davi Santos'

print(nome_completo.upper())
print(nome_completo.lower())
print(nome_completo.title())
print(nome_completo.capitalize())
print(nome_completo.swapcase())